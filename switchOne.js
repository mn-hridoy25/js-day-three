var date = new Date();
var today = date.getDay();

switch (today) {
    case 0:
        console.log("Sun Day");
        break;
    case 1:
        console.log("Mon Day");
        break;
    case 2:
        console.log("Tues Day");
        break;
    case 3:
        console.log("Wednes Day");
        break;
    case 4:
        console.log("Thurs Day");
        break;
    case 5:
        console.log("Fri Day");
        break;
    case 6:
        console.log("satur Day");
        break;
}